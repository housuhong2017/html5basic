		function changeStyle(name){
			var bk =document.getElementById("content");
			var change=bk.style.background="gray";
			localStorage.setItem('as',change);
			window.onload=function(){
				document.getElementsByTagName('p').background=localStorage.getItem('as');
			}
		}
